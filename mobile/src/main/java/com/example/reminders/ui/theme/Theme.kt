/**
 * Root theme composable for the Reminders app.
 *
 * Applies the full design system:
 * - **Colours**: Dynamic (Material You) on Android 12+, teal fallback otherwise.
 * - **Typography**: Custom [RemindersTypography] with DM Sans weights.
 * - **Shapes**: Modern large-radius [RemindersShapes].
 * - **Motion**: M3 Expressive motion scheme for fluid animations.
 *
 * @param darkTheme   Whether to use the dark colour scheme.
 * @param dynamicColor Whether to use Material You dynamic colours (Android 12+).
 * @param content     The composable content tree.
 */
@Composable
fun RemindersTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> TealDarkColorScheme
        else -> TealLightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = RemindersTypography,
        shapes = RemindersShapes,
        motionScheme = MotionScheme.expressive(),
        content = content
    )
}
